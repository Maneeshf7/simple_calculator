op=input("enter the operator +,-,*,/: ")

num1=float(input("enter the number :"))
num2=float(input("enter the number :" ))
if op=="+":
    result=num1+num2
    print(result)
elif op=="-":
    result=num1-num2
    print(result)
elif op=="*":
    result=num1*num2
    print(result)
elif op=="/":
    result=num1/num2
    print(result)
